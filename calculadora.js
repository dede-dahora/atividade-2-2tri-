function soma(a, b) {
    return a + b;
}

function subtracao(a, b) {
    return a - b;
}

function multiplicacao(a, b) {
    return a * b;
}

function divisao(a, b) {
    return a / b;
}

console.log("Soma: 5 + 3 =", soma(5, 3));
console.log("Subtração: 10 - 4 =", subtracao(10, 4));
console.log("Multiplicação: 7 * 6 =", multiplicacao(7, 6));
console.log("Divisão: 20 / 5 =", divisao(20, 5));